#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*ps;

	ps = (char *)s;
	while (*ps)
	{
		if (*ps == c)
			return (ps);
		ps++;
	}
	if (*ps == c)
		return (ps);
	return (NULL);
}
